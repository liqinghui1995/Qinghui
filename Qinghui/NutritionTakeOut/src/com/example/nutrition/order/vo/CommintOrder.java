package com.example.nutrition.order.vo;

public class CommintOrder {

	private int id;
	private String biches;
	private String biches_num;
	private String biches_price;
	private String biches_money;
	public CommintOrder(int id, String biches, String biches_num,
			String biches_price, String biches_money) {
		super();
		this.id = id;
		this.biches = biches;
		this.biches_num = biches_num;
		this.biches_price = biches_price;
		this.biches_money = biches_money;
	}
	public CommintOrder(String biches, String biches_num, String biches_price,
			String biches_money) {
		super();
		this.biches = biches;
		this.biches_num = biches_num;
		this.biches_price = biches_price;
		this.biches_money = biches_money;
	}
	
	
	public CommintOrder() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getBiches() {
		return biches;
	}
	public void setBiches(String biches) {
		this.biches = biches;
	}
	public String getBiches_num() {
		return biches_num;
	}
	public void setBiches_num(String biches_num) {
		this.biches_num = biches_num;
	}
	public String getBiches_price() {
		return biches_price;
	}
	public void setBiches_price(String biches_price) {
		this.biches_price = biches_price;
	}
	public String getBiches_money() {
		return biches_money;
	}
	public void setBiches_money(String biches_money) {
		this.biches_money = biches_money;
	}
	@Override
	public String toString() {
		return "CommintOrder [id=" + id + ", biches=" + biches
				+ ", biches_num=" + biches_num + ", biches_price="
				+ biches_price + ", biches_money=" + biches_money + "]";
	}
	
}
