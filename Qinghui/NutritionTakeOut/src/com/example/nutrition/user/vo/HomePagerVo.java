package com.example.nutrition.user.vo;

import java.io.Serializable;

public class HomePagerVo implements Serializable{

	
	private static final long serialVersionUID = 1L;
	
	private int id;
	private String viewpagerurl;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getViewpagerurl() {
		return viewpagerurl;
	}
	public void setViewpagerurl(String viewpagerurl) {
		this.viewpagerurl = viewpagerurl;
	}
	public HomePagerVo(int id, String viewpagerurl) {
		super();
		this.id = id;
		this.viewpagerurl = viewpagerurl;
	}
	public HomePagerVo() {
		super();
	}
	@Override
	public String toString() {
		return "HomePagerVo [id=" + id + ", viewpagerurl=" + viewpagerurl + "]";
	}
	

}
