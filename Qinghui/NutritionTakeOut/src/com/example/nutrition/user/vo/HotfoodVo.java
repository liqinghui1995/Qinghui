package com.example.nutrition.user.vo;

import java.io.Serializable;
import java.util.List;

import com.example.nutrition.diningroom.vo.DiningRoom;
import com.example.nutrition.nourishment.bean.NourishmentFood;

public class HotfoodVo implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private List<DiningRoom> diningRoomList;
	private List<NourishmentFood> foodList;
	public List<DiningRoom> getDiningRoomList() {
		return diningRoomList;
	}
	public void setDiningRoomList(List<DiningRoom> diningRoomList) {
		this.diningRoomList = diningRoomList;
	}
	public List<NourishmentFood> getFoodList() {
		return foodList;
	}
	public void setFoodList(List<NourishmentFood> foodList) {
		this.foodList = foodList;
	}
	
	public HotfoodVo() {
		super();
	}
	public HotfoodVo(List<DiningRoom> diningRoomList,
			List<NourishmentFood> foodList) {
		super();
		this.diningRoomList = diningRoomList;
		this.foodList = foodList;
	}
	@Override
	public String toString() {
		return "ListDiningRoom [diningRoomList=" + diningRoomList
				+ ", foodList=" + foodList + "]";
	}
	
}
