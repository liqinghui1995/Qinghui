package com.example.nutrition.diningroom.vo;

import java.io.Serializable;

public class Gifts implements Serializable{
	private int id,price;
	private String name,imgurl;
	public Gifts(int id, int price, String name, String imgurl) {
		super();
		this.id = id;
		this.price = price;
		this.name = name;
		this.imgurl = imgurl;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getImgurl() {
		return imgurl;
	}
	public void setImgurl(String imgurl) {
		this.imgurl = imgurl;
	}
	@Override
	public String toString() {
		return "Gifts [id=" + id + ", price=" + price + ", name=" + name
				+ ", imgurl=" + imgurl + "]";
	}
	
}
